package com.modeln.batam.ant.typedef;

public class Info {
	private String name;
	
	private String value;

	public Info() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
